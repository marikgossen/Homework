package class8;

public class E5WhileBreak {
    public static void main(String[] args) {

        boolean summer=true;
        int temp=85;
        while(summer){

             if(temp<100){
                 System.out.println("The summer is nice");
             temp=temp+3;
             } else {
                 System.out.println("It is too hot outside");
                break;
             }


         }









    }



}
