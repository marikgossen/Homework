package groupProject;

public class task05 {
    public static void main(String[] args) {

        int a=10;
        int b=20;

        a=a+b;//30
        b=a-b;//10
        a=a-b;//20
        System.out.println("After swap :");
        System.out.println("a = "+a);
        System.out.println("b = "+b);
        System.out.println("-----------------------------------------------");

        a=10;
        b=20;

        a=a*b;
        b=a/b;
        a=a/b;
        System.out.println("After swap :");
        System.out.println("a = "+a);
        System.out.println("b = "+b);







    }
}
